function getLastYYMMDD() {
    const tDate = new Date()
    const date = new Date(tDate.getTime() - 24 * 60 * 60 * 1000)
    return getYYMMDD(date)
}

function getYYMMDD(date) {
    if (!date) {
        date = new Date()
    }
    const YY_S = date.getFullYear().toString()
    const YY = YY_S.substring(YY_S.length - 2)
    const MM_S = (date.getMonth() + 1).toString()
    const MM = MM_S.padStart(2, '0')
    const DD_S = date.getDate().toString()
    const DD = DD_S.padStart(2, '0')
    return YY + MM + DD
}

